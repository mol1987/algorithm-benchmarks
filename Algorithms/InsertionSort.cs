    [Benchmark]
    public void InsertionSortRun() 
    {
        var data = AutoGenerateData();
        if (data == null)
        {
            Console.WriteLine("Invalid value set in DataSize");
            return;
        }

		InsertionSort(data);

	}
	void InsertionSort(int[] data)
	{
		//Gör en loop för varje tal som skall sorteras 
		//börja på index 1 då vi kommer att titta "bakåt" i vektorn
		for (int i = 1; i < data.Length; i++)
		{
			//Stega bakåt från position i ned till 1 om så behövs
			for (int j = i; j > 0; j--)
			{
				//jämför med talet "bakom" och se om det är större
				if (data[j] < data[j - 1])
				{
					//byt plats på tal
					int tmp = data[j - 1];
					data[j - 1] = data[j];
					data[j] = tmp;
				}
				//annars avsluta innerloop'en 
				else
					break;
			}
		}
	}


}